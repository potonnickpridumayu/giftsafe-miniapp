// Денежный формат: округление ВНИЗ до 2 знаков, как на биржах — экран
// никогда не показывает больше, чем реально можно потратить.
// 0.9719 → "0.97", 0.085 → "0.08". Эпсилон гасит float-хвосты (58.85 ≠ 58.84).
export const fmtGram = (n) =>
  (Math.floor((Number(n) || 0) * 100 + 1e-7) / 100).toFixed(2)
